public class ConversaoDeDados1 {
    public static double quilometroParaMetro(double quilometro){
        return quilometro * 1000;
    }
    
    public static double litroParaDecilitros(double litro){
        return litro * 10; 
    }
    
    public static double metroCubicoParaPesCubicos(double metroCubico){
        return metroCubico * 35.31;
    }
    
    public static double barrilParaDecalitros(double barril){
        return barril * 16.36;
    }
    
    public static double barrilParaLitros(double barril){
        return barril * 163.65;
    }
}
